package com.xworkz.differentmethods;

public class JavaMath {
public static void main(String []args) {
	int x=10;
	int y=20;
	System.out.println("Biggest number is:" +Math.max(x,y));
	System.out.println("Square root of x is:" +Math.sqrt(x));
	System.out.println("smallest number is:" +Math.min(x,y));
	System.out.println("log10 of y is:" +Math.log(y));

}
}
